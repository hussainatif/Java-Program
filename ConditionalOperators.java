class ConditionalOperators
{
     public static void main(String args[])
     {
 
         int a=10;
         int b= 20;
         int value1 = (a<b) ? a : b;
         int value2 = (a>b) ? a : b;
 
         System.out.println("value1 : "+value1);
         System.out.println("value2 : "+value2);
 
     }
}